#!/usr/bin/env python3

cipher = [0x41, 0xf5, 0x51, 0xd1, 0x4d, 0x61, 0xd5, 0xe9, 0x69, 0x89, 0x19, 0xdd, 0x09, 0x11, 0x89, 0xcb, 0x9d, 0xc9, 0x69, 0xf1, 0x6d, 0xd1, 0x7d, 0x89, 0xd9, 0xb5, 0x59, 0x91, 0x59, 0xb1, 0x31, 0x59, 0x6d, 0xd1, 0x8b, 0x21, 0x9d, 0xd5, 0x3d, 0x19, 0x11, 0x79, 0xdd]


for i in range(21):
    cipher[i], cipher[42-i] = cipher[42-i], cipher[i]

for i, c in enumerate(cipher):
    cipher[i] = 0
    for _ in range(8):
        cipher[i] <<= 1
        cipher[i] |= (c & 0x1)
        c >>= 1

for i, c in enumerate(cipher):
    cipher[i] = chr(~c & 0xff)

print(''.join(cipher))

